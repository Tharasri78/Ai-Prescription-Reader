import React, { useState, useEffect } from 'react';
import { useNavigate, useLocation } from 'react-router-dom'; // ✅ ADDED useLocation
import Navbar from '../../components/Navbar/Navbar'; 
import MedicineCard from '../../components/MedicineCard/MedicineCard';
import './Results.css';

const Results = () => {
  const navigate = useNavigate();
  const location = useLocation(); // ✅ ADDED
  const [isVisible, setIsVisible] = useState(false);

  useEffect(() => {
    setIsVisible(true);
  }, []);

  // 🔥 REPLACED SAMPLE DATA WITH REAL DATA
  const medicines = location.state?.medicines || [];

  const handleNewScan = () => {
    navigate('/upload');
  };

  return (
    <div className="results-container">
      {/* Animated Background */}
      <div className="results-gradient-bg">
        <div className="gradient-sphere sphere-1"></div>
        <div className="gradient-sphere sphere-2"></div>
        <div className="gradient-sphere sphere-3"></div>
      </div>

      {/* Add Navbar */}
      <Navbar isVisible={isVisible} />

      {/* Results Content */}
      <div className={`results-content ${isVisible ? 'content-visible' : ''}`}>
        <div className="results-header">
          <div className="results-title-section">
            <h1>Prescription Results</h1>
            <p>We found {medicines.length} medicines in your prescription</p>
          </div>
          
          <div className="results-actions">
            <button className="action-btn primary" onClick={handleNewScan}>
              New Scan
            </button>
          </div>
        </div>

        {/* 🔥 EMPTY STATE HANDLING */}
        {medicines.length === 0 ? (
          <h2>No medicines detected</h2>
        ) : (
          <div className="medicines-grid">
            {medicines.map((medicine, index) => (
              <MedicineCard key={index} medicine={medicine} />
            ))}
          </div>
        )}

        {/* Summary Section */}
        <div className="results-summary">
          <h3>Prescription Summary</h3>
          <div className="summary-cards">
            <div className="summary-card">
              <div className="summary-info">
                <span className="summary-label">Total Medicines</span>
                <span className="summary-value">{medicines.length}</span>
              </div>
            </div>
            
            <div className="summary-card">
              <div className="summary-info">
                <span className="summary-label">Daily Doses</span>
                <span className="summary-value">
                  {/* 🔥 simple dynamic calc */}
                  {medicines.length * 2}
                </span>
              </div>
            </div>
            
            <div className="summary-card">
              <div className="summary-info">
                <span className="summary-label">Duration</span>
                <span className="summary-value">
                  {medicines[0]?.duration || 'N/A'}
                </span>
              </div>
            </div>
          </div>
        </div>

        {/* Disclaimer */}
        <div className="results-disclaimer">
          <p>
            ⚕️ This information is generated by AI. Please consult your doctor or pharmacist 
            before taking any medication.
          </p>
        </div>
      </div>
    </div>
  );
};

export default Results;